var disp1 = document.getElementById("records1");
var display1 = 0;
function showhide1() {
    if (display1 == 1) {
        disp1.style.display = "block";
        display1 = 0;
    }
    else {
        disp1.style.display = "none";
        display1 = 1;
    }

}

